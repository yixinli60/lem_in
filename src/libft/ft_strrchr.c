#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *str;

	str = ft_strlen(s) + (char *)s;
	while (*str != c)
	{
		if (str == s)
			return (NULL);
		str--;
	}
	return (str);
}
