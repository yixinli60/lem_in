#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	str = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*str != (unsigned char)c)
			str++;
		else
			return (str);
		i++;
	}
	return (NULL);
}
