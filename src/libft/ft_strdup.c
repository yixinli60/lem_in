#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*cpy;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s[i])
		i++;
	cpy = malloc(i + 1);
	if (!cpy)
		return (NULL);
	while (s[j] != '\0')
	{
		cpy[j] = s[j];
		j++;
	}
	cpy[j] = '\0';
	return (cpy);
}
